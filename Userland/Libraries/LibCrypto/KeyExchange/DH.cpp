#include "DH.h"
#include <LibCrypto/NumberTheory/ModularFunctions.h>

namespace Crypto::KeyExchange {

void DH::generate_shared_secret(Bytes& output) const
{
    // y^x mod p
    auto tmp = NumberTheory::ModularPower(public_key().y, private_key().x, private_key().p);

    // write it out to the output
    auto size = tmp.export_data(output);
    output = output.trim(size);
}

}
